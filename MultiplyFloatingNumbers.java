public class MultiplyFloatingNumbers {
    public static void main(String args[]){
       float f1 = 12.2f;
       System.out.println("First floating point number:: " + f1);
       float f2 = 6.3f;
       System.out.println("Second floating point number:: " + f2);
       float product = f1 * f2;
       System.out.println("Product of given floating point numbers:: " + product);
    }
 }
 